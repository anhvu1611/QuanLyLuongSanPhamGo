    protected void btnTimKiemActionPerformed(ActionEvent e) {
		String maSP = txtMa.getText();
		String tenSP = txtTenSP.getText();
		String khoiLuong = txtKhoiLuong.getText();
		String soLuong = txtSoLuong.getText();
		String dai = txtDai.getText();
		String rong = txtRong.getText();
		String cao = txtCao.getText();
		Date ngay = dscThoiHan.getDate();
		String gia = txtGia.getText();
		LoaiGo loaiGo = (LoaiGo) cmbLoaiGo.getSelectedItem();
		String soCongDoan = (String) cmbSoCongDoan.getSelectedItem();
		ArrayList<SanPham> ds = SanPham_Dao.timKiem(maSP, tenSP, khoiLuong, soLuong, loaiGo, soCongDoan, dai, rong, cao, ngay, gia);
		DefaultTableModel modal = (DefaultTableModel) tblCongNhan.getModel();
		int i=0;
		modal.setRowCount(0);
		for (SanPham sanPham : ds) {
			modal.addRow(new String[] {++i+"", sanPham.getMaSanPham(), sanPham.getTenSP(), sanPham.getKichThuoc(), sanPham.getSoLuong()+"", sanPham.getGiaThanh()+"", sanPham.getChatGo().toString(), sanPham.getTrangThai()+"", sanPham.getThoiGianSanXuatDuKien()+""});
		}
	}

