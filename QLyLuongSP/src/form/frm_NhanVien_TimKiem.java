    private Object layDuLieuTimKiem(Component com) {
    	if(com.isEnabled()) {
    		if(com instanceof JTextField) {
    			return (String)((JTextField) com).getText().trim();
    		}else if(com instanceof JComboBox) {
    			return ((JComboBox<?>) com).getSelectedItem();
    		}else if(com instanceof JDateChooser) {
    			return ((JDateChooser) com).getDate();
    		}else if(com instanceof JCheckBox) {
    			return ((JCheckBox) com).isSelected();
    		}
    	}else {
    		if(com instanceof JCheckBox)
    			return false;
    		return null;
    	}
		return null;
    }
    
