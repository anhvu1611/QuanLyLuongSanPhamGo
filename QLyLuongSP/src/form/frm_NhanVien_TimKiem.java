	private void checkBoxSuLySuKien(JCheckBox chk,ItemEvent e, JComponent txt, String moTa){
        if (e.getStateChange() == ItemEvent.SELECTED) {
                    txt.setEnabled(true);
        } else {
            if(txt instanceof JTextField) {
            	((JTextField) txt).setText(moTa);
                txt.setEnabled(false);
            }else if(txt instanceof JComboBox) {
            	txt.setEnabled(false);
            }else {
            	txt.setEnabled(false);
            }
        }
    }

    private void checkBoxSuLySuKien(JCheckBox chk,ItemEvent e, JComponent txt1, JComponent txt2, String moTa1, String moTa2){
        if (e.getStateChange() == ItemEvent.SELECTED) {
                    txt1.setEnabled(true);
                    txt2.setEnabled(true);
        } else if(txt1 instanceof JTextField && txt2 instanceof JTextField) {
            ((JTextField)txt1).setText(moTa1);
            ((JTextField)txt2).setText(moTa2);
            txt1.setEnabled(false);
            txt2.setEnabled(false);
        }else {
        	((JCheckBox) txt1).setSelected(false);
        	((JCheckBox) txt2).setSelected(false);
        	txt1.setEnabled(false);
            txt2.setEnabled(false);
        }
    }
    
    private void checkBoxSuLySuKien(JCheckBox chk,ItemEvent e, JComboBox<String> txt1, JComboBox<String> txt2, JComboBox<String> txt3){
        if (e.getStateChange() == ItemEvent.SELECTED) {
                    txt1.setEnabled(true);
                    txt2.setEnabled(true);
                    txt3.setEnabled(true);
        } else {
            txt1.setEnabled(false);
            txt2.setEnabled(false);
            txt3.setEnabled(false);
        }
    }
    
