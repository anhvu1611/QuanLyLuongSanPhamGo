        tblCongNhan.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int row = tblCongNhan.rowAtPoint(e.getPoint());
                if (e.getButton() == MouseEvent.BUTTON1 && row >= 0) {
                    String ma = (String) tblCongNhan.getValueAt(row, 1);
                    CongNhan nv = qlCongNhan.timCongNhanTheoMa(ma);
                    ThongTinNhanVienDialog nhanVienDialog = new ThongTinNhanVienDialog(new Frame() , true);
                    nhanVienDialog.chuyenThanhFormCongNhan();
                    nhanVienDialog.caiDuLieuVaoForm(nv.getAnh(), nv.getMaNhanSu(), nv.getHo()+" "+ nv.getTen(),
                    								nv.getChuyenMon().getTenChuyenMon(), nv.getGioiTinh().getGioiTinh(),
                    								nv.getNgaySinh().toString(), nv.getNgayVaoLam().toString(),
                    								nv.getCCCD(), nv.getSoDienThoai(), (nv.isTrangThai()?"Tham gia":"Không"),
                    								nv.getPhuCap()+"", nv.getDiaChi().toString(), "");
                    nhanVienDialog.setLocationRelativeTo(null);
                    nhanVienDialog.setVisible(true);
                }
            }
        });
