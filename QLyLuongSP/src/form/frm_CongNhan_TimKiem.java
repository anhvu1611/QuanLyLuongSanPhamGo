    protected void btnTimActionPerformed(ActionEvent evt) {
    	try {
    		String maCongNhan = "";
        	String ten = "";
        	String ho = "";
        	String cccd = "";
        	String sdt = "";
        	GioiTinh gioiTinh = null;
        	String tinhThanh = "";
        	String quanHuyen = "";
        	String thiXa = "";
        	Date ngaySinh = null;
        	Date ngayVaoLam = null;
        	ChuyenMon chuyenMon = null;
        	
        	maCongNhan = (String) layDuLieuTimKiem(txtMa);
        	chuyenMon = (ChuyenMon) layDuLieuTimKiem(cmbChuyenMon);
        	gioiTinh = (GioiTinh) layDuLieuTimKiem(cmbGioiTinh);
        	ho = (String) layDuLieuTimKiem(txtHo);
        	ten = (String) layDuLieuTimKiem(txtTen);
        	cccd = (String) layDuLieuTimKiem(txtCanCuoc);
        	sdt = (String) layDuLieuTimKiem(txtSDT);
        	tinhThanh = (String) layDuLieuTimKiem(cmbThanhPho);
        	quanHuyen = (String) layDuLieuTimKiem(cmbQuanHuyen);
        	thiXa =(String) layDuLieuTimKiem(cmbXa);
        	ngaySinh = (Date) layDuLieuTimKiem(dcsNgaySinh);
        	ngayVaoLam = (Date) layDuLieuTimKiem(dcsNgayVaoLam);
        	CongNhan mangCongNhan[] = qlCongNhan.tieuChiTimKiemCongNhan(maCongNhan, chuyenMon, gioiTinh,ho, ten, cccd, sdt, tinhThanh, quanHuyen, thiXa, ngaySinh, ngayVaoLam);
        	loadDuLieuNhanVienRaBang(modal, mangCongNhan);
        	lamMoiDuLieu();
		} catch (Exception e) {
			// TODO: handle exception
			e.printStackTrace();
		}
	}

